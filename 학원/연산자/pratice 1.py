print(1+1) # 1 + 1을 계산함 2
print(1-1) # 1 - 1을 계산함 0
print(1*2) # 1 * 2를 계산함 2
print(4/2) # 4 / 2의 몫을 계산함 2
print(3%2) # 3 % 2의 나머지를 계산함 1

print(2**3) # 2^3 8
print(5//3) # 5를 3으로 나누었을때의 몫 1

print(10 > 3) # ture 출력
print(10 < 3) # false 출력
print(10 >= 3) # 10이 3보다 크거나 같을 때 ture
print(3 >= 3) # 3이 3보다 크거나 같을 때 ture
print(10 <= 3) #3이 10보다 크거나 같을 때 false

print(3 == 3) # 3이 3과 같을 때 ture
print(3 != 3) # 3이 3과 다를 때 false
print(3 != 4) # 3이 4와 다를 때 ture
print(not(1 != 3)) # 1이 3과 다를 때면 ture 지만 앞에 not에 의해서 false로 바뀜

print((1 > 3) and (1 < 3)) # false and 때문에 둘 다 ture 가 되어야 함
print((1 > 3) & (1 < 3)) # 위 and와 똑같음 and = & 
print((1 > 3) or (1 < 3)) # 1 < 3이 ture 이기 때문에 ture
print((1 > 3) | (1 < 3)) # 위 or과 똑같음 or = |   

print(5 > 4 > 3) # ture 모든 결과가 맞음 
print(1 < 2 < 1) # false 모든 결과가 맞아야 함
